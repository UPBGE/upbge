def catalog_status_for_idname(node_idname):
    if not node_idname.startswith("LogicNative"):
        return LOGIC_NODE_STATUS_IMPLEMENTED
    entry = LOGIC_NODE_PARITY_CATALOG.get(node_idname)
    if entry is None:
        return LOGIC_NODE_STATUS_UNTRACKED
    return entry["status"]


def catalog_native_idnames(*, status=None, visible=None):
    idnames = set()
    for entry in LOGIC_NODE_PARITY_CATALOG.values():
        if status is not None and entry["status"] != status:
            continue
        if visible is not None and entry["visible"] != visible:
            continue
        idnames.add(entry["idname"])
    return frozenset(idnames)


def deferred_native_idnames():
    return frozenset(
        entry["idname"]
        for entry in LOGIC_NODE_DEFERRED_CATALOG.values()
        if entry.get("idname")
    )
