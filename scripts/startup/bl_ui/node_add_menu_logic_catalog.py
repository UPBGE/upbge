LOGIC_NODE_STATUS_IMPLEMENTED = "implemented"
LOGIC_NODE_STATUS_DEFERRED = "deferred"
LOGIC_NODE_STATUS_DROPPED = "dropped"
LOGIC_NODE_STATUS_UNTRACKED = "untracked"


def _implemented(idname, label, menu_path, family, *, phase="Phase 4"):
    return {
        "idname": idname,
        "label": label,
        "menu_path": menu_path,
        "family": family,
        "status": LOGIC_NODE_STATUS_IMPLEMENTED,
        "visible": True,
        "phase": phase,
    }


def _deferred(key, label, family, reason, *, idname=None, target_phase="Phase 4 closure",
              infrastructure_blocker=None, menu_path=None):
    return {
        "key": key,
        "idname": idname,
        "label": label,
        "menu_path": menu_path,
        "family": family,
        "status": LOGIC_NODE_STATUS_DEFERRED,
        "visible": False,
        "phase": target_phase,
        "reason": reason,
        "infrastructure_blocker": infrastructure_blocker,
    }


def _dropped(key, label, family, reason, *, menu_path=None, target_phase="Phase 7"):
    return {
        "key": key,
        "idname": None,
        "label": label,
        "menu_path": menu_path,
        "family": family,
        "status": LOGIC_NODE_STATUS_DROPPED,
        "visible": False,
        "phase": target_phase,
        "reason": reason,
    }


