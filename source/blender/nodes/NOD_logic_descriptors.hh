inline Span<NodeMetadata> logic_node_metadata()
{
  return detail::metadata_span(detail::all_node_metadata);
}

inline const NodeMetadata *find_logic_node_metadata(const StringRef idname)
{
  for (const NodeMetadata &metadata : logic_node_metadata()) {
    if (idname == metadata.idname) {
      return &metadata;
    }
  }
  return nullptr;
}

